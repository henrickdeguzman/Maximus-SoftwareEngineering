    private void updateDentalRecordActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_updateDentalRecordActionPerformed
    {//GEN-HEADEREND:event_updateDentalRecordActionPerformed
        try
        {
            java.sql.Date sqlDate = new java.sql.Date(birthday.getDate().getTime());
            PreparedStatement pstmt = con.prepareStatement("UPDATE PATIENT_RECORD SET LAST_NAME = ?, FIRST_NAME = ?, MIDDLE_INIT = ?, ADDRESS = ?, CONTACT_NUM = ?, BIRTHDAY = ? WHERE PATIENT_ID = ?");
            pstmt.setString(1,LastName_DentalRecord.getText());
            pstmt.setString(2,FirstName_DentalRecord.getText());
            pstmt.setString(3,MiddleInitial_DentalRecord.getText());
            pstmt.setString(4,jTextArea1.getText());
            pstmt.setString(5,Contact_DentalRecord.getText());
            pstmt.setDate(6, sqlDate);
            pstmt.setString(7,updatePatientID.getText().trim());
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Changed!");
            pstmt.close();
            
             
            String ln=LastName_DentalRecord.getText();
            String fn=FirstName_DentalRecord.getText();
            String id = updatePatientID.getText().trim();
            String ts = ZonedDateTime.now().format(DateTimeFormatter.ofLocalizedDateTime(FormatStyle.FULL, FormatStyle.MEDIUM));
            String event = "Updated Patient  Record  of Patient ID # : " +id + " Name: "+ ln +", "+fn+".";
            
            String qry2 = "INSERT INTO HISTORY(DATE, EVENT) VALUES(?,?)";
            
            PreparedStatement pstmt2 = con.prepareStatement(qry2);
            pstmt2.setString(1, ts);
            pstmt2.setString(2,event);
            pstmt2.executeUpdate();
            
            PatientRecords dr = new PatientRecords();
            new RowPopup(dr);
            new RowPopup2(dr);
            dr.setVisible(true);
            super.dispose();
        }
        catch (Exception ex)
        {
            System.out.println(ex);
        }
    }//GEN-LAST:event_updateDentalRecordActionPerformed

