    public UpdatePatientRecord(String patId, String ln, String fn)
    {
        initComponents();
        try
        {
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            String url = "jdbc:derby://localhost:1527/Dental Clinic";
            String user = "maximus";
            String pass = "maximus123";
            con = DriverManager.getConnection(url, user, pass);
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM PATIENT_RECORD ORDER BY PATIENT_ID");
            while(rs.next())
            {
                if(rs.getString("PATIENT_ID").equals(patId) && rs.getString("LAST_NAME").equals(ln) && rs.getString("FIRST_NAME").equals(fn))
                {
                    updatePatientID.setText(rs.getString("PATIENT_ID"));
                    LastName_DentalRecord.setText(rs.getString("LAST_NAME"));
                    FirstName_DentalRecord.setText(rs.getString("FIRST_NAME"));
                    MiddleInitial_DentalRecord.setText(rs.getString("MIDDLE_INIT"));
                    jTextArea1.setText(rs.getString("ADDRESS"));
                    Contact_DentalRecord.setText(rs.getString("CONTACT_NUM"));
                    birthday.setDate(rs.getDate("BIRTHDAY"));
                }
            }
        }
        catch(Exception e)
        {
            
        }
    }

